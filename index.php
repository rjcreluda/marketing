  <script>
      $(document).ready( function(){
        $('#contact').on('submit', function(e){
            e.preventDefault();
            $.ajax({
                type: 'POST',
                url: 'handler.php',
                data: {
                    name: $('#name').val(),
                    surname: $('#surname').val(),
                    email: $('#email').val(),
                    subject: $('#subject').val(),
                    message: $('#message').val(),
                },
                success: function( response ){
                    Swal.fire(
                      'Good job!',
                      'You message was sent successfully',
                      'success'
                    ).then( ( result) => {
                        $('#name').val(""),
                        $('#surname').val(""),
                        $('#email').val(""),
                        $('#subject').val(""),
                        $('#message').val("")
                    } );
                    $("#contact").trigger("reset");
                    console.log( response );
                },
                error: function( response ){
                    Swal.fire({
                      icon: 'error',
                      title: 'Oops...',
                      text: 'Something went wrong! Try again later'
                    })
                    console.log( response )
                },
            })
            
        });
      });
  </script>

